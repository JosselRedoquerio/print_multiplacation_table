# Exercise 13: Print multiplication table from 1 to 10

print("\n")
print("multiplication table 1 to 10:")
for x in range(1, 11):
    for y in range(1, 11):
        print(x * y, end= " ")
    print(" ")

print("\n")

print("multiplication table 1 to 20:")
for a in range(1, 21):
    for b in range(1, 21):
        print(a * b, end= " ")
    print("")

print("\n")